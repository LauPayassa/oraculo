export const translateCardName = (name) => {
  let translated = name;
  
  // Traduzir partes do nome
  Object.entries(translations).forEach(([en, pt]) => {
    translated = translated.replace(new RegExp(en, 'gi'), pt);
  });
  
  return translated;
};

